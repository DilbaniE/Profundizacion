export const sumar = (a: number, b:number): number =>{
    return(a+b);
    
}

export const divi = (a: number, b:number): number =>{
    return(a/b);
    
}

export const restar = (a: number, b:number): number =>{
    return(a-b);
    
}

export const multi = (a: number, b:number): number =>{
    return(a*b);
    
}